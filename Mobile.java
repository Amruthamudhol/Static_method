class Mobile {

    static void call() {
        System.out.println("Calling from mobile..");
    }

    static void message() {
        System.out.println("Sending a message..");
    }

    static void browseInternet() {
        System.out.println("Browsing the internet..");
    }

    static void playMusic() {
        System.out.println("Playing music..");
    }

    static void takePhoto() {
        System.out.println("Taking a photo..");
    }

    static void recordVideo() {
        System.out.println("Recording a video..");
    }

    static void checkBattery() {
        System.out.println("Checking battery level..");
    }

    static void useGPS() {
        System.out.println("Using GPS navigation..");
    }

    static void installApp() {
        System.out.println("Installing an application..");
    }

    static void lockPhone() {
        System.out.println("Locking the mobile phone..");
    }

    public static void main(String[] args) {

        call();
        message();
        browseInternet();
        playMusic();
        takePhoto();
        recordVideo();
        checkBattery();
        useGPS();
        installApp();
        lockPhone();
    }
}
